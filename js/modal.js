////// 공중전화팀 소개
    var modal3 = document.querySelector(".modal3"); 
    var trigger3 = document.querySelector(".trigger3"); //버튼class명
    var closeButton3 = document.querySelector(".close-button3"); 
    var cancelButton3 = document.querySelector("#cancel3");

   //console.log(modal);

   function toggleModal3() {  //팝업클래스
        modal3.classList.toggle("show-modal3"); //팝업클래스
    }

   function windowOnClick3(event) { 
        if (event.target === modal3) { 
            toggleModal3(); 
        } 
    }

    trigger3.addEventListener("click", toggleModal3); //버튼클래스를 클릭할 때 = 팝업클래스출력
    closeButton3.addEventListener("click", toggleModal3); 
    cancel3.addEventListener("click", toggleModal3); 
    window.addEventListener("click", windowOnClick3); 

